    private void btnReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReporteActionPerformed
        Map p=new HashMap();
        JasperReport report;
        JasperPrint print;
        
        try {
            report=JasperCompileManager.compileReport(new File("").getAbsolutePath()+//creando un objeto file archivo vacio obteniendo su directorio absoluto indicando la ruta de nuestro proyecto
                    "/src/Reportes/rpthabitaciones.jrxml");// uvicar la ruta del reporte
            //enviamos la ruta de nuestro reporte  
            print=JasperFillManager.fillReport(report, p,connection);
            JasperViewer view=new JasperViewer(print,false);
            //propiedades de jasperReport
            view.setTitle("Reporte de Habitaciones");
            view.setVisible(true);            
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnReporteActionPerformed

