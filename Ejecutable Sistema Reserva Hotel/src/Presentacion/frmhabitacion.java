    private void tablelistadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablelistadoMouseClicked
        btnguardar.setText("Editar");
        habilitar();
        btneliminar.setEnabled(true);
        accion = "editar";
        // se le asigna las coordenadas del registro seleccionado con el indice
        int fila = tablelistado.rowAtPoint(evt.getPoint()); //cptamos la posicion de la fila y  sus coordenadas
        //Pasar datos a cada campo del formulario
        //Obtenemos el dato de la tabla de su columna convertimos a cadena  porque viene como objeto
        txtidhabitacion.setText(tablelistado.getValueAt(fila, 0).toString());
        txtnumero.setText(tablelistado.getValueAt(fila, 1).toString());
        cbopiso.setSelectedItem(tablelistado.getValueAt(fila, 2).toString());
        txtdescripcion.setText(tablelistado.getValueAt(fila, 3).toString());
        txtcaracteristicas.setText(tablelistado.getValueAt(fila, 4).toString());
        txtpreciodiario.setText(tablelistado.getValueAt(fila, 5).toString());
        cboestado.setSelectedItem(tablelistado.getValueAt(fila, 6).toString());
        cbotipo_habitacion.setSelectedItem(tablelistado.getValueAt(fila, 7).toString());
    }//GEN-LAST:event_tablelistadoMouseClicked

